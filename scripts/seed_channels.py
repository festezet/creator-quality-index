def seed_channels():
    """Seed the benchmark database with curated YouTube channels."""
    conn = get_connection(DB_PATH)

    inserted = 0
    skipped = 0
    tier_counts = {"S": 0, "A": 0, "B": 0, "C": 0, "D": 0}

    for ch in CHANNELS:
        # Compute composite score and tier via the scoring module
        scores = {
            "score_research_depth": ch["score_research_depth"],
            "score_production": ch["score_production"],
            "score_signal_noise": ch["score_signal_noise"],
            "score_originality": ch["score_originality"],
            "score_lasting_impact": ch["score_lasting_impact"],
        }
        composite, tier = score_channel(scores)

        # INSERT OR IGNORE keyed on channel_id (UNIQUE constraint)
        cursor = conn.execute(
            """INSERT OR IGNORE INTO channels (
                channel_id, name, url, platform, language,
                primary_category, description,
                score_research_depth, score_production,
                score_signal_noise, score_originality, score_lasting_impact,
                composite_score, tier,
                scoring_notes, sample_videos, is_reviewed
            ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, 1)""",
            (
                ch["channel_id"],
                ch["name"],
                ch["url"],
                ch["platform"],
                ch["language"],
                ch["primary_category"],
                ch["description"],
                ch["score_research_depth"],
                ch["score_production"],
                ch["score_signal_noise"],
                ch["score_originality"],
                ch["score_lasting_impact"],
                composite,
                tier,
                ch["scoring_notes"],
                ch["sample_videos"],
            ),
        )

        if cursor.rowcount > 0:
            inserted += 1
            tier_counts[tier] += 1
            print(f"  + {ch['name']:40s} | {tier} ({composite:.2f}) | {ch['primary_category']}")
        else:
            skipped += 1
            tier_counts[tier] += 1
            print(f"  = {ch['name']:40s} | already exists, skipped")

    conn.commit()
    conn.close()

    # --- Summary ---
    print("\n" + "=" * 60)
    print(f"Seed complete: {inserted} inserted, {skipped} skipped (already present)")
    print(f"Total channels in seed data: {len(CHANNELS)}")
    print("\nTier distribution (seed data):")
    for t in ["S", "A", "B", "C", "D"]:
        bar = "#" * tier_counts[t]
        print(f"  {t}: {tier_counts[t]:3d}  {bar}")

    # Category distribution
    cat_counts = {}
    for ch in CHANNELS:
        cat_counts[ch["primary_category"]] = cat_counts.get(ch["primary_category"], 0) + 1
    print("\nCategory distribution:")
    for cat, count in sorted(cat_counts.items(), key=lambda x: -x[1]):
        print(f"  {cat:25s}: {count}")


if __name__ == "__main__":
    print(f"Seeding channels into {DB_PATH}")
    print(f"Total channels to seed: {len(CHANNELS)}\n")
    seed_channels()
